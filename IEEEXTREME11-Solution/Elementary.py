def solve():
	s=input().lower()
	dp=[0]*1000
	dp[0]=1
	size=len(s)
	for i in range(1,size+1):
		now=s[:i]
		for j in d:
			if now.endswith(j):
				x=i-len(j)
				if x<0:break
				dp[i]+=dp[x]
	print(dp[size])
n=int(input())
for i in range(n):
	solve()
